// COMPOSITION fixture for the fetch-now route's FORM-1 + FORM-3 fix — replaces the prod-touching
// stored-state harness with a pure, fixture-based assertion (cannot touch prod). Asserts the full
// decision (kind + HTTP status + which last_* stamp), forcing each outcome via injected inputs.
import { test } from "node:test";
import assert from "node:assert/strict";
import { decideFetchOutcome } from "../../src/lib/sources/fetch-now-decision.mjs";

// Mirrors the route: it always stamps last_checked, and ADDS last_inaccessible / last_accessible
// only when decision.stamp says so. This helper is what the route does with the decision.
function stampsFor(decision) {
  const u = { last_checked: true };
  if (decision.stamp.inaccessible) u.last_inaccessible = true;
  if (decision.stamp.accessible) u.last_accessible = true;
  return u;
}

test("FORM 1 — a 429 non-answer is INCONCLUSIVE (503), and does NOT stamp last_inaccessible", () => {
  const d = decideFetchOutcome({ error: Object.assign(new Error("rate limited"), { status: 429 }) });
  assert.equal(d.kind, "inconclusive");
  assert.equal(d.httpStatus, 503);
  assert.equal(stampsFor(d).last_inaccessible, undefined); // a rate-limit can't mark a live source dead
});
test("FORM 1 — a timeout/network throw (no status) is INCONCLUSIVE, not inaccessible", () => {
  const d = decideFetchOutcome({ error: new Error("ETIMEDOUT") });
  assert.equal(d.kind, "inconclusive");
  assert.equal(stampsFor(d).last_inaccessible, undefined);
});
test("FORM 1 — a definitive-dead 404 IS inaccessible (502 + last_inaccessible)", () => {
  const d = decideFetchOutcome({ error: Object.assign(new Error("API 404: Not Found"), { status: 404 }) });
  assert.equal(d.kind, "dead");
  assert.equal(d.httpStatus, 502);
  assert.equal(stampsFor(d).last_inaccessible, true);
});
test("MUTATION (FORM 1): 429 and 404 must DIFFER — a no-op that treated both as dead would fail here", () => {
  const r429 = decideFetchOutcome({ error: Object.assign(new Error("x"), { status: 429 }) });
  const r404 = decideFetchOutcome({ error: Object.assign(new Error("x"), { status: 404 }) });
  assert.notEqual(r429.kind, r404.kind);
});

test("FORM 3 — a 200 with an error/bot-block BODY is INCONCLUSIVE (503), NO last_accessible", () => {
  const errBody = "403 ERROR\nThe request could not be satisfied.\nGenerated by cloudfront (CloudFront)";
  const d = decideFetchOutcome({ content: errBody });
  assert.equal(d.kind, "inconclusive");
  assert.equal(d.reason, "error-body");
  assert.equal(stampsFor(d).last_accessible, undefined); // an error page is not "accessible"
});
test("OK — genuine content is reachable (200 + last_accessible)", () => {
  const real = "Regulation (EU) 2023/956 establishing a carbon border adjustment mechanism. ".repeat(6);
  const d = decideFetchOutcome({ content: real });
  assert.equal(d.kind, "ok");
  assert.equal(d.httpStatus, 200);
  assert.equal(stampsFor(d).last_accessible, true);
});
test("MUTATION (FORM 3): error-body and real content must DIFFER", () => {
  const err = decideFetchOutcome({ content: "Just a moment... Attention Required! Cloudflare" });
  const ok = decideFetchOutcome({ content: "Real long regulatory document body. ".repeat(10) });
  assert.notEqual(err.kind, ok.kind);
});
